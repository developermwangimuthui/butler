            <h2 class="text-primary">OUR FLEET POLICY</h2>


            <p>Butler Logistics (K) Ltd and its Management is committed to managing its fleet in a manner that will preserve and protect the environment and guarantee the safety and health of its employees and the general public.</p>
            <p>The Fleet Management Policy is based and aligned to statutory requirements, established Fleet management systems, Butler Logistics (K) Ltd requirements, and other requirements.</p>
            <p>The policy ensures that Butler Logistics (K) Ltd business objectives are continuously met. </p>
            <p>This policy will apply to all Butler Logistics (K) Ltd operations & outsourced services.</p>
            <p>Butler Logistics (K) Ltd shall ensure that;</p>
            <ul>
              <li>All relevant stakeholders are involved in the development, implementation, and compliance of the Fleet Policy.</li>
              <li>The Fleet policy is communicated to the relevant stakeholders and is continuously appraised on this policy, statutory requirements, and The Butler Logistics (K) Ltd operating requirements. </li>
              <li>The Fleet Policy is aligned to our business objectives and safety targets</li>
              <li>The Fleet maintenance facility is adequate, well equipped, and safe </li>
              <li>Fleet management routines and audits remain relevant and up to done external service providers with continual reviews and closure of corrective actions to ensure continuous improvement. </li>
              <li>Engagement of competent Fleet maintenance service providers on maintenance activities to ensure the entire fleet is safe for operation at all times.</li>
              <li>All fleet users are competent to operate the equipment.</li>
              <li>Procurement of assets and third-party suppliers meet environment and sates, standards.</li>
              <li>Fleet stakeholders are trained on environmental awareness to ensure that the related aspects are managed in an environmentally friendly manner.</li>
              <li>Fleet incidents and accidents are reported and investigated in a manner that protects all stakeholders' interests.</li>
              <li>Fleet operations within and outside the business premises meet safety requirements.</li>

            </ul>

          </p>



